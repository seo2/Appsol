			            <div class="col-lg-11"><h5>4. Colector Cilíndrico - Parabólico (PTC)</h5> 
			            <h3>Principio de funcionamiento</h3><br>
						Un colector cilíndrico-parabólico es, un reflector especular con sección transversal parabólica cuya función es concentrar la radiación directa sobre un tubo absorbente, que se encuentra en su línea focal. El coeficiente de concentración de esta tecnología es el más elevado, sobre los 70-80, por lo que las temperaturas alcanzables son las más elevadas, en cuanto a tecnologías de colectores de media temperatura se refiere.<br><br> 
