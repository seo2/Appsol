			            <div class="col-lg-11"><h5>2. Colectores sin Cubierta (UFPC)</h5><br>
						Este tipo de colectores están concebidos para temperaturas de operación muy bajas, en torno a los 35°C (calentamiento de piscinas, evaporadores de bombas de calor, precalentamiento de procesos térmicos industriales). Por este motivo no tiene cubierta de cristal ni aislamiento, con lo que no es necesario una caja metálica para alojar estos elementos y en consecuencia son constructivamente más sencillos que los FPC. <br><br>
						Esta simplificación constructiva queda justificada por la notable reducción de costos que representa y que, para ciertas aplicaciones de muy baja temperatura, puede compensar la pérdida de rendimiento que conlleva.<br><br>
