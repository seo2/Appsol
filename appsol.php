			            <div class="col-lg-11"><h5>3. Colectores Solares de Tubo Vacío (ETC)</h5><br>
						Como su nombre indica, los colectores de tubo de vacío están formados por hileras de tubos de cristal conectados en paralelo a una tubería de cabecera. En cada uno de estos tubos de vidrio se ha realizado el vacío a fin de reducir al máximo las pérdidas de calor por conducción y convección interna. Su geometría tubular es necesaria para soportar la diferencia de presión entre la atmosfera y el vacío del interior.<br><br>
						El nivel de vacío es la característica esencial para mantener la eficiencia de los colectores de tubo de vacío, a la par que la propiedad cuya fiabilidad a largo plazo es más difícil de garantizar. Por consiguiente la calidad de los materiales y las garantías propuestas por el fabricante merecen especial atención para este tipo de tecnología.<br><br>
